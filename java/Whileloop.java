public class Whileloop {
    public static void main(String[] args) {
        int i = 0;
        while (i < 5) {
            if (i == 0) {

                System.out.println("In If ------>> " + i);
                i++;
            } else {
                System.out.println("In Loop............:----" + i);
                i++;

            }

        }
    }
}
