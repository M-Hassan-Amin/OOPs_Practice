#include <iostream>
using namespace std;

int main() {
    // Assign variables with different data types
    string name = "Ali";
    int age = 25;
    double salary = 55000.75;

    // Print the values along with their data types
    cout << "Name: " << name << " (string)" << endl;
    cout << "Age: " << age << " (int)" << endl;
    cout << "Salary: " << salary << " (double)" << endl;

    return 0;
}
